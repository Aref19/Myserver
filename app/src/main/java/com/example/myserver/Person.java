package com.example.myserver;

public class Person {
    String name;
    String Email;
    String strasse;
    String haus;
    String plz;


    public String getStrasse() {
        return strasse;
    }

    public String getPlz() {
        return plz;
    }

    public String getHaus() {
        return haus;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return Email;
    }
}
